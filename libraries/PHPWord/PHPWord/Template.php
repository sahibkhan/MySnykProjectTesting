    public function setValue($search, $replace) {
        $breaks = array("<br />","<br>","<br/>", "&lt;br/&gt;", "&lt;br /&gt;");  
        $replace = str_ireplace($breaks, "\r\n", $replace);
        $replace = htmlspecialchars($replace, ENT_QUOTES,"UTF-8");
        $replace = preg_replace("~\R~u", "</w:t><w:br/><w:t>", strip_tags($replace));
         if(substr($search, 0, 2) !== '${' && substr($search, -1) !== '}') 
         { 
             //было - $search = '${'.$search.'}';
          $search = '/\$\{.*?'.$search.'.*?\}/is'; }
           if(!is_array($replace)) { 
               //$replace = utf8_encode($replace);
               //$replace = $this->xmlEntities($replace);
            } 
               //было - $this->_documentXML = str_replace($search, $replace, $this->_documentXML);
                $this->_documentXML = preg_replace($search, $replace, $this->_documentXML); 
            } 

    
    /**
     * Save Template
     * 
     * @param string $strFilename
     */
    public function save($strFilename) {
        if(file_exists($strFilename)) {
            unlink($strFilename);
        }
        
        $this->_objZip->addFromString('word/document.xml', $this->_documentXML);
        
        // Close zip file
        if($this->_objZip->close() === false) {
            throw new Exception('Could not close zip file.');
        }
        
        rename($this->_tempFileName, $strFilename);
    }

